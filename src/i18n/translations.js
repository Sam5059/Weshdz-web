// Fonction helper pour récupérer une traduction
export const t = (key, lang = 'fr') => {
  const keys = key.split('.');
  let value = translations[lang];

  for (const k of keys) {
    value = value?.[k];
  }

  if (!value) {
    console.warn(`Translation missing for key: ${key}`);
    return key;
  }

  return value;
};
